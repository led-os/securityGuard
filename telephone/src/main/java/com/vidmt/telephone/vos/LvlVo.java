package com.vidmt.telephone.vos;

public class LvlVo {
	private String type;
	private float money;
	private int expire;

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public float getMoney() {
		return money;
	}

	public void setMoney(float money) {
		this.money = money;
	}

	public int getExpire() {
		return expire;
	}

	public void setExpire(int expire) {
		this.expire = expire;
	}

}
